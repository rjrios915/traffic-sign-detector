def build_annots(df):
    annots = {}
    for _, row in df.iterrows():
        fname = row["filename"]
        if fname not in annots:
            annots[fname] = []
        annots[fname].append([row["x1"], row["y1"], row["x2"], row["y2"], row["class_id"]])
    return annots

